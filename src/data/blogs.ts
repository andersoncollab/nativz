/** Get all blog posts, sorted by publish date (newest first) */
export function getAllBlogPosts(): BlogPost[] {
    return [...blogPosts].sort(
        (a, b) => new Date(b.publishDate).getTime() - new Date(a.publishDate).getTime()
    );
}

/** Get a blog post by slug */
export function getBlogPost(slug: string): BlogPost | undefined {
    return blogPosts.find((p) => p.slug === slug);
}

/** Get blog posts by category */
export function getBlogsByCategory(category: string): BlogPost[] {
    return getAllBlogPosts().filter((p) =>
        p.categories.map((c) => c.toLowerCase()).includes(category.toLowerCase())
    );
}

/** Get all unique categories */
export function getAllCategories(): string[] {
    const cats = new Set<string>();
    blogPosts.forEach((p) => p.categories.forEach((c) => cats.add(c)));
    return Array.from(cats).sort();
}

/** Get all unique tags */
export function getAllTags(): string[] {
    const tags = new Set<string>();
    blogPosts.forEach((p) => p.tags.forEach((t) => tags.add(t)));
    return Array.from(tags).sort();
}
